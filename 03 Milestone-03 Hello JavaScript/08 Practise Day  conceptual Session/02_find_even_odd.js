// even ==> 0 2 4 6 8 10 .......
// odd ==> 1 3 5 7 9 11 ........


// Even Number condition: i % 2 == 0
for(let i = 1; i<=10; i++){
    if(i % 2 === 0){
        console.log(i);
    }
}

// Odd Number condition: i % 2 == 1
for(let i = 1; i<=10; i++){
    if(i % 2 === 1){
        console.log(i);
    }
}